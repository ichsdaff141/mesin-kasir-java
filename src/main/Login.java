    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String pass = new String(txtPassword.getPassword());
        String user = txtUsernane.getText();
        try {
            String sql = "SELECT * FROM `tb_login` WHERE `username`= '"+user+"' AND `password`=MD5('"+pass+"')";
            java.sql.Connection conn = (Connection) Config.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet result = stm.executeQuery(sql);
            
            if(result.next()){
                if(result.getString("level").equals("admin")){
                    JOptionPane.showMessageDialog(null, "berhasil login");
                    Admin admin = new Admin();
                    admin.setVisible(true);
                    dispose();
                } 
                else if (result.getString("level").equals("kasir")){
                    JOptionPane.showMessageDialog(null, "berhasil login");
                    MesinKasir mesinkasir = new MesinKasir();
                    mesinkasir.setVisible(true);
                    dispose();
                }
                else {
                   JOptionPane.showMessageDialog(null, "username atau password salah");
                }
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnLoginActionPerformed

